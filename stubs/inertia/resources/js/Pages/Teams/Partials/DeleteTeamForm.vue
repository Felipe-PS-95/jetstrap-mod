<template>
  <jet-action-section>
    <template #title>
      Delete Team
    </template>

    <template #description>
      Permanently delete this team.
    </template>

    <template #content>
      <div>
        Once a team is deleted, all of its resources and data will be permanently deleted. Before deleting this team, please download any data or information regarding this team that you wish to retain.
      </div>

      <div class="mt-3">
        <jet-danger-button @click="confirmTeamDeletion">
          Delete Team
        </jet-danger-button>
      </div>

      <!-- Delete Team Confirmation Modal -->
      <jet-confirmation-modal id="confirmingTeamDeletionModal">
        <template #title>
          Delete Team
        </template>

        <template #content>
          Are you sure you want to delete this team? Once a team is deleted, all of its resources and data will be permanently deleted.
        </template>

        <template #footer>
          <jet-secondary-button data-bs-dismiss="modal">
            Cancel
          </jet-secondary-button>

          <jet-danger-button class="ms-2" @click="deleteTeam" :class="{ 'text-white-50': form.processing }" :disabled="form.processing">
            <div v-show="form.processing" class="spinner-border spinner-border-sm" role="status">
              <span class="visually-hidden">Loading...</span>
            </div>

            Delete Team
          </jet-danger-button>
        </template>
      </jet-confirmation-modal>
    </template>
  </jet-action-section>
</template>

<script>
import { defineComponent } from 'vue'
import JetActionSection from '@/Jetstream/ActionSection.vue'
import JetButton from '@/Jetstream/Button.vue'
import JetConfirmationModal from '@/Jetstream/ConfirmationModal.vue'
import JetDangerButton from '@/Jetstream/DangerButton.vue'
import JetSecondaryButton from '@/Jetstream/SecondaryButton.vue'

export default defineComponent({
  props: ['team'],

  components: {
    JetActionSection,
    JetButton,
    JetConfirmationModal,
    JetDangerButton,
    JetSecondaryButton,
  },

  data() {
    return {
      modal: null,
      deleting: false,

      form: this.$inertia.form()
    }
  },

  methods: {
    confirmTeamDeletion() {
      let el = document.querySelector('#confirmingTeamDeletionModal')
      this.modal = new bootstrap.Modal(el)
      this.modal.show()
    },

    deleteTeam() {
      this.form.delete(route('teams.destroy', this.team), {
        errorBag: 'deleteTeam',
        onSuccess: () => this.modal.hide()
      });
    },
  },
})
</script>
